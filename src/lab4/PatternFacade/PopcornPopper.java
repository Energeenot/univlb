package lab4.PatternFacade;

public class PopcornPopper {
    public void on() {
        System.out.println("Машина для попкорна включена");
    }

    public void pop() {
        System.out.println("Машина для попкорна готовит попкорн");
    }

    public void off() {
        System.out.println("Машина для попкорна выключена");
    }
}
