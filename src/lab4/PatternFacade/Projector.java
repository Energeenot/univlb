package lab4.PatternFacade;

public class Projector {
    public void on() {
        System.out.println("Проектор включён");
    }

    public void wideScreenMode() {
        System.out.println("Выбран широкоэкранный режим для кино");
    }

    public void off() {
        System.out.println("Проектор выключен");
    }
}
